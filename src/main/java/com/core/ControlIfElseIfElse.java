package com.core;

public class ControlIfElseIfElse {

	public static void main(String[] args) {
		int a=10;
		int b=7;
		int c=9;
		
		if((a<c) && (a<b)) {
			System.out.println("a is small");
		}
		else if(b<c) {
			System.out.println("b is small");
		}
		else {
			System.out.println("c is small");
		}
	}

}
