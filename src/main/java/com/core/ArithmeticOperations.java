package com.core;

public class ArithmeticOperations {

	public static void main(String[] args) {
		//arithmetic operators
		int a=10;
		int b=3;
		
		//addition
		System.out.println(a+b);
		
		//subtraction
		System.out.println(a-b);
		
		//multiplication
		System.out.println(a*b);
		
		//division
		System.out.println(a/b);
		
		//modulus
		System.out.println(a%b);
		
		
	}

}
